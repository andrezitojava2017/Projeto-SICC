    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed

        // verificação do atributo, caso seja ZERO, a busca sera feita na tabela pessoas cujo sexo SEJA MASCULINO
        // caso contrario, sera feito uma busca na tabela de REGISTRANDO
        switch (opcao_tabela) {
            case 0: {
                // fazendo a sql completa
                String dadoPesquisa = campoLocalizar.getText();
                String condicao = "Masculino";
                // instanciando objeto
                Localizar_Controller pessoas = new Localizar_Controller();
                // passsando a sql completa para o metodo controller que ira chamar o metodo da camada DAO
                // retornando um List com as informações capturadas.
                List<Pessoas_Model> resultado = pessoas.LocalizardadosPessoa(dadoPesquisa, condicao);
                // passando dados como parametro para o metodo que ira preencher a tabela
                preencherTabela(resultado);
                break;
            }
            case 2: {
                // fazendo a sql completa
                String dadoPesquisa = campoLocalizar.getText();
                String condicao = "Feminino";
                // instanciando objeto
                Localizar_Controller pessoas = new Localizar_Controller();
                // passsando a sql completa para o metodo controller que ira chamar o metodo da camada DAO
                // retornando um List com as informações capturadas.
                List<Pessoas_Model> resultado = pessoas.LocalizardadosPessoa(dadoPesquisa, condicao);
                // passando dados como parametro para o metodo que ira preencher a tabela
                preencherTabela(resultado);
                break;
            }
            case 1: {
                // fazendo a sql completa
                String dadoPesquisa = campoLocalizar.getText();
                // instanciando objeto
                Nascimento_Controller localizar_registrando = new Nascimento_Controller();
                // passsando a sql completa para o metodo controller que ira chamar o metodo da camada DAO
                // retornando um List com as informações capturadas.
                ArrayList<Pessoas_Model> resultado = localizar_registrando.localizar_registrando(dadoPesquisa);
                // passando dados como parametro para o metodo que ira preencher a tabela
                preencher_tabela_dados_registrado(resultado);
                break;
            }
        }


    }//GEN-LAST:event_btnBuscarActionPerformed

