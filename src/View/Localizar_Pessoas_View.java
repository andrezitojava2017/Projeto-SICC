    /**
     * Metodo que preenche a tabela com dados recuperados do banco de dados
     *
     * @param retorno
     */
    public void preencherTabela(List<Pessoas_Model> retorno) {

        DefaultTableModel tabela = (DefaultTableModel) tblResultadoPesquisa.getModel();
        tabela.setNumRows(0);

        for (Pessoas_Model res : retorno) {

            tabela.addRow(new Object[]{
                res.getId(),
                res.getNome()
            });
        }
    }

    /**
     * Metodo que preenche a tabela com as informações do registrando
     * @param dados_registrado 
     */
    private void preencher_tabela_dados_registrado(ArrayList<Pessoas_Model> dados_registrado) {

        DefaultTableModel tabela = (DefaultTableModel) tblResultadoPesquisa.getModel();
        tabela.setNumRows(0);

        for (int i = 0; i < dados_registrado.size(); i++) {
            // instanciando objetos do tipo Certidao e Pessoas
            Pessoas_Model registrando = new Pessoas_Model();
            registrando = dados_registrado.get(i);
            tabela.addRow(new Object[]{
                registrando.getId(),
                registrando.getNome()
            });
        }
    }

