    private void btn_buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_buscarActionPerformed

        if (campo_data_fim.getText().isEmpty() || campo_data_inicio.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Preencha os campos de data inicio e data fim", "Mensagem", JOptionPane.ERROR_MESSAGE);
        } else {

            String data_inicio = campo_data_inicio.getText();
            String data_fim = campo_data_fim.getText();

            DateTimeFormatter form = DateTimeFormatter.ofPattern("dd/MM/yyyy");
            LocalDate d1 = LocalDate.parse(data_inicio, form);
            LocalDate d2 = LocalDate.parse(data_fim, form);

            sql_busca = "select cadastro.tblselos.data_ato, cadastro.tblselos.letras, cadastro.tblselos.selos, cadastro.tblselos.custo, cadastro.tblselos.impresso, cadastro.tblselos.id from cadastro.tblselos inner join cadastro.tblautenticacao on cadastro.tblautenticacao.id_selo = cadastro.tblselos.id where cadastro.tblselos.data_ato >=' " + d1 + " ' " + " and cadastro.tblselos.data_ato <=' " + d2 + " ' ";

            Autenticacao_Controller control = new Autenticacao_Controller();
            List<Selos_Model> autenticacoes = control.buscar_autenticacao(sql_busca);
            preencher_tabela_autenticaoes(autenticacoes);
        }
    }//GEN-LAST:event_btn_buscarActionPerformed

