    private void check_todas_autenticacaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_check_todas_autenticacaoActionPerformed

        // se a busca for por todas as autenticações lançadas, incluindo as não impressas
        // passamos esta sql para o controller
        if (check_todas_autenticacao.isSelected()) {

            check_auten_nao_impressa.setSelected(false);
            sql_busca = "select cadastro.tblselos.id, cadastro.tblselos.data_ato, cadastro.tblselos.letras, cadastro.tblselos.selos, cadastro.tblselos.custo, cadastro.tblselos.impresso from cadastro.tblselos inner join cadastro.tblautenticacao on cadastro.tblautenticacao.id_selo = cadastro.tblselos.id where cadastro.tblselos.impresso ='S' OR cadastro.tblselos.impresso ='N' ";

            Autenticacao_Controller control = new Autenticacao_Controller();
            List<Selos_Model> autenticacoes = control.buscar_autenticacao(sql_busca);
            preencher_tabela_autenticaoes(autenticacoes);

        }

    }//GEN-LAST:event_check_todas_autenticacaoActionPerformed

