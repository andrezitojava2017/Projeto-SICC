    private void preencher_tabela_autenticaoes(List<Selos_Model> dados) {

        DefaultTableModel tabela = (DefaultTableModel) tbl_autenticacoes.getModel();
        tabela.setNumRows(0);

        for (Selos_Model dado : dados) {

            tabela.addRow(new Object[]{
                dado.getId_selo(),
                dado.getseqLetras() + dado.getseqNumeros(),
                dado.getData_ato(),
                dado.getSituacao_impresso()
            });
        }
    }

