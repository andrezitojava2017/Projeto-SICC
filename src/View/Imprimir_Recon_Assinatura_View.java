    private void btn_buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_buscarActionPerformed

        String data_inicio = campo_data_inicial.getText();
        String data_fim = campo_data_fim.getText();

        DateTimeFormatter form = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        LocalDate d1 = LocalDate.parse(data_inicio, form);
        LocalDate d2 = LocalDate.parse(data_fim, form);

        String sql_busca = "select cadastro.tblselos.data_ato, cadastro.tblselos.letras, cadastro.tblselos.selos, cadastro.tblselos.impresso, cadastro.tblselos.id from cadastro.tblselos inner join cadastro.tblreconfirma on cadastro.tblreconfirma.id_selo = cadastro.tblselos.id where cadastro.tblselos.data_ato >=' " + d1 + " ' " + " and cadastro.tblselos.data_ato <=' " + d2 + " ' ";

        Reconhecer_Assinatura_Controller control = new Reconhecer_Assinatura_Controller();
        List<Selos_Model> info_selos = control.buscar_selos_utilizado_recon_firma(sql_busca);

        preencher_tabela_selos(info_selos);

    }//GEN-LAST:event_btn_buscarActionPerformed

    private void check_opt_periodoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_check_opt_periodoActionPerformed

        if (check_opt_periodo.isSelected()) {
            campo_data_fim.setEnabled(true);
            campo_data_inicial.setEnabled(true);
            btn_buscar.setEnabled(true);
            campo_data_inicial.requestFocus();
        } else {
            campo_data_fim.setEnabled(false);
            campo_data_inicial.setEnabled(false);
            btn_buscar.setEnabled(false);
        }
    }//GEN-LAST:event_check_opt_periodoActionPerformed

    private void btn_removerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_removerActionPerformed

        DefaultTableModel tabela = (DefaultTableModel) tbl_selos_selecionados.getModel();
        linha_selecionada = tbl_selos_selecionados.getSelectedRow();

        if (tabela.getRowCount() != 0) {
            tabela.removeRow(linha_selecionada);
            contador--;
        } else {
            JOptionPane.showMessageDialog(this, "Ops... a tabela de selos para serem impressos não esta preenchida", "Mensagem", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btn_removerActionPerformed

    private void tbl_selos_selecionadosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_selos_selecionadosMouseClicked
        // atribuindo valor ao atributo linha_selecionada, assim ativamos o botao remover
        // para remover linhas da tabela de selos selecionados
        linha_selecionada = tbl_selos_selecionados.getSelectedRow();
        btn_remover.setEnabled(true);

    }//GEN-LAST:event_tbl_selos_selecionadosMouseClicked

