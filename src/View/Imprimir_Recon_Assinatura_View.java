    private void tbl_selosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_selosMouseClicked

        if (evt.getClickCount() == 1) {
            int linha = tbl_selos.getSelectedRow();
            id_selo = tbl_selos.getValueAt(linha, 0).toString();
            preencher_tabela_pessoas(id_selo);

        } else if (evt.getClickCount() == 2) {
            int linha = tbl_selos.getSelectedRow();
            id_selo = tbl_selos.getValueAt(linha, 0).toString();
            preencher_lista_selecionada(id_selo);
        }

    }//GEN-LAST:event_tbl_selosMouseClicked

