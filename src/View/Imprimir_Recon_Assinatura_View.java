    private void btn_imprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_imprimirActionPerformed

        // capturando os valores dos IDs dos selos que foram selecionados
        DefaultTableModel tabela = (DefaultTableModel) tbl_selos_selecionados.getModel();
        int total_linhas = tabela.getRowCount();

        // atributo que ira armazenar os selos escolhidos para impressão
        int selos_selecionados[] = {0, 0, 0, 0};

        if (total_linhas != 0) {

            // add valores no vetor sequencia[]
            for (int i = 0; i < total_linhas; i++) {
                int valor = Integer.parseInt(tabela.getValueAt(i, 0).toString());
                selos_selecionados[i] = valor;
            }

            // iremos alterar os valores dos combobox da tela de impressao de selos,
            // indicando assim as opções de relatorios
            String opt_localizacao[] = new String[2];

            opt_localizacao[0] = "Superior";
            opt_localizacao[1] = "Inferior";

            Imprimir_Relatorio rel = new Imprimir_Relatorio(null, false, opt_localizacao, Codigo_Atos_Model.CODIGO_RECONHECIMENTO, selos_selecionados);
            rel.setVisible(true);

        } else {
            JOptionPane.showMessageDialog(this, "Atenção nenhum selo de reconhecimento foi selecionado", "Mensagem", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_imprimirActionPerformed

