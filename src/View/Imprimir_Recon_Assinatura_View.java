            String sql = "select cadastro.tblselos.id, cadastro.tblselos.letras, cadastro.tblselos.selos, cadastro.tblselos.data_ato, cadastro.tblselos.impresso from cadastro.tblselos inner join cadastro.tblreconfirma on cadastro.tblreconfirma.id_selo = cadastro.tblselos.id";

