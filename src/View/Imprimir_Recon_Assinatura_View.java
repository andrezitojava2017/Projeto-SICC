    /**
     * Preenche a tabela com as informações dos selos que foram utilizados em
     * reconhecimento de assinaturas
     *
     * @param dados
     */
    private void preencher_tabela_selos(List<Selos_Model> dados) {

        DefaultTableModel tabela = (DefaultTableModel) tbl_selos.getModel();
        tabela.setNumRows(0);

        for (Selos_Model dado : dados) {

            tabela.addRow(new Object[]{
                dado.getId_selo(),
                dado.getseqLetras() + dado.getseqNumeros(),
                dado.getData_ato(),
                dado.getSituacao_impresso()
            });
        }
    }

    private void preencher_tabela_pessoas(String id_selo) {
        DefaultTableModel tabela = (DefaultTableModel) tbl_pessoas.getModel();
        tabela.setNumRows(0);

        Reconhecer_Assinatura_Controller control = new Reconhecer_Assinatura_Controller();

        List<Pessoas_Model> pessoas = control.buscar_pessoa_recon_firma(id_selo);
        for (Pessoas_Model dado : pessoas) {
            tabela.addRow(new Object[]{
                dado.getId(),
                dado.getNome(),
                dado.getCpf()
            });
        }
    }

    private void preencher_lista_selecionada(String selo_selecionado) {

        // verificando o total de permissao de posições no vetor
        if (contador <= 3) {
            DefaultTableModel tabela = (DefaultTableModel) tbl_selos_selecionados.getModel();
            String sqls = "select cadastro.tblselos.id, cadastro.tblselos.letras, cadastro.tblselos.selos, cadastro.tblselos.custo from cadastro.tblselos where cadastro.tblselos.id=" + selo_selecionado;

            Reconhecer_Assinatura_Controller control = new Reconhecer_Assinatura_Controller();
            Selos_Model info_selos = control.preencher_tabela_selos_selecionados(sqls);

            tabela.addRow(new Object[]{
                info_selos.getId_selo(),
                info_selos.getseqLetras() + info_selos.getseqNumeros(),
                info_selos.getCusto()
            });
            contador++;

//            sequencia[contador] = info_selos.getId_selo();
//            System.out.println("Valor vetor[" + contador + "] = " + info_selos.getId_selo());
//            System.out.println("Incrementando contador...");
        } else {
            JOptionPane.showMessageDialog(this, "Quantidade maxima de selos permitidos alcançado", "Mensagem", JOptionPane.ERROR_MESSAGE);
        }

    }

