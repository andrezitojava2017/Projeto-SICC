    private void btnAbrirFichaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAbrirFichaActionPerformed
        try {
            // local onde esta localizado a copia da ficha de assinatura
            String local_ficha = campoLocalFicha.getText();

            if (local_ficha.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Campo local ficha não foi fornecido", "Mensagem", JOptionPane.ERROR_MESSAGE);
            } else {

                File imag = new File(local_ficha);
                Desktop.getDesktop().open(imag);
            }
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, "Erro ao tentar abrir a ficha de assinatura deste usuario\n", "ERROR", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnAbrirFichaActionPerformed

