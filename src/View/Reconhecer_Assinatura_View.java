    /**
     * Metodo que preenche os campos da tela reconhecimento de assinatura,
     *
     * @param idPessoa - int idPessoa parametro recebido pela JDialog de
     * Consulta de pessoas
     */
    private void preencherCamposAssinatura(int idPessoa) {

        String id_selecao = String.valueOf(idPessoa);

        // objeto para capturar dados da tabela pessoa
        Localizar_Controller buscar = new Localizar_Controller();

        // objeto com dados de uma determinada pessoa
        Pessoas_Model pessoa = buscar.capturarDadosPessoaSelecionada(id_selecao);

        // capturando dados da ficha de assinatura da pessoa selecionada
        Ficha_Assinatura_Controller buscar_ficha = new Ficha_Assinatura_Controller();
        Ficha_Assinatura_Model ficha_model = new Ficha_Assinatura_Model();

        ficha_model = buscar_ficha.Capturar_Dados_Ficha_Assinatura(idPessoa);
        // preenchimento dos campos
        campoId.setText(String.valueOf(pessoa.getId()));
        campoNome.setText(pessoa.getNome());
        campoIdentidade.setText(pessoa.getDocIdentidade());
        campoExpeditor.setText(pessoa.getOrgaoExpeditor());
        campoCPF.setText(pessoa.getCpf());
        campoFicha.setText(String.valueOf(ficha_model.getNumero_ficha()));
        campoLocalFicha.setText(ficha_model.getLocal_ficha());

    }

