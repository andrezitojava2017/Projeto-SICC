    private void btnSalvarNovaCertidaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarNovaCertidaoActionPerformed
        // salvar info da certidao apresentada
        Certidao_Controller control = new Certidao_Controller();
        Certidao_Model certidao = new Certidao_Model();
        
        certidao.setLivro(Integer.parseInt(campoLivro.getText()));
        certidao.setFolha(Integer.parseInt(campoFolha.getText()));
        certidao.setTermo(Integer.parseInt(campoTermo.getText()));
        certidao.setNome_cartorio(campoCartorioRegistro.getText());
        certidao.setTipo_certidao(comboTpCertidao.getSelectedItem().toString());
        certidao.setCidade_registro(campoCidadeRegistro.getText());
        certidao.setEstado_reg(comboEstadoRegistro.getSelectedItem().toString());
        
        this.id_certidao = control.Cadastrar_Nova_Certidao(certidao);
    }//GEN-LAST:event_btnSalvarNovaCertidaoActionPerformed

