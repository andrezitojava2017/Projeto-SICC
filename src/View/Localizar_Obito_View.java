    /**
     * Preenche a tabela com os dados de um falecido
     *
     * @param falecidos
     */
    private void preencher_tabela_falecidos(List<Pessoas_Model> falecidos) {

        DefaultTableModel tabela = (DefaultTableModel) tbl_falecidos.getModel();
        tabela.setNumRows(0);

        for (Pessoas_Model falecido : falecidos) {

            tabela.addRow(new Object[]{
                falecido.getId(),
                falecido.getNome(),
                falecido.getMae()
            });
        }
    }

    /**
     * Preenche a tabela registro de obitos, com as informações escolhida na
     * tabela_falecidos
     *
     * @param falecido
     */
    private void preencher_tabela_registro_obito(List<Object> falecido) {

        DefaultTableModel tabela = (DefaultTableModel) tbl_registro_obito.getModel();
        tabela.setNumRows(0);

        Certidao_Model certidao = (Certidao_Model) falecido.get(0);
        Selos_Model selo = (Selos_Model) falecido.get(1);

        tabela.addRow(new Object[]{
            certidao.getId_certidao(),
            certidao.getLivro(),
            certidao.getFolha(),
            certidao.getTermo(),
            selo.getseqLetras() + selo.getseqNumeros()
        });

    }

