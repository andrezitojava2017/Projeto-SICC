    private void campo_pesquisaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_campo_pesquisaKeyPressed

        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            Localizar_Obito_Controller localizar = new Localizar_Obito_Controller();
            List<Pessoas_Model> falecido = localizar.buscar_pessoa_falecida(campo_pesquisa.getText());
            preencher_tabela_falecidos(falecido);
        }
    }//GEN-LAST:event_campo_pesquisaKeyPressed

