    private void tbl_falecidosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_falecidosMouseClicked

        List<Object> dados = new ArrayList<>();
        
        if (evt.getClickCount() == 2) {

            int linha = tbl_falecidos.getSelectedRow();
            this.id_selecionado = Integer.parseInt(tbl_falecidos.getValueAt(linha, 0).toString());

            dispose();
            
        } else {

            int linha = tbl_falecidos.getSelectedRow();
            this.id_selecionado = Integer.parseInt(tbl_falecidos.getValueAt(linha, 0).toString());
            Localizar_Obito_Controller localizar = new Localizar_Obito_Controller();
            dados = localizar.capturar_dados_obito(id_selecionado);
            preencher_tabela_registro_obito(dados);
        }
    }//GEN-LAST:event_tbl_falecidosMouseClicked

