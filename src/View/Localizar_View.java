    /**
     * Metodo que ativa os botoes da tela Consulta conforme cada tela que chama
     *
     * @param alt - boolean ativa o botao Alterar - quando chamada da tela
     * Abertura de firma
     * @param recFir - boolean ativa o botao reconhecer assin. - quando cham.
     * tela reconhecer Ass.
     * @param lancProc - boolean ativa o botao lançar em procuração - quando
     * cham tela procuracao
     * @return
     */
    public int ativarBotoesConsultar(boolean alt, boolean recFir, boolean lancProc) {

        if (alt == true) {
            btnAlterarDados.setEnabled(true);
        } else if (recFir == true) {
            btnReconhecerAssinat.setEnabled(true);
        } else if (lancProc == true) {
            btnProcuracao.setEnabled(true);
        }

        // exibir tela
        this.setVisible(true);

        return id_referencia;
    }

    public void read(List<Pessoas_Model> dados) {
        /**
         * Para mostrar os dados na tabela que criamos, precisamos alterar o
         * model(), recuperando o model, dentro desse metodo temos um atributo
         * String nomeP, que recebe valores que ira ser pesquisado no BD.
         */
        DefaultTableModel modelo = (DefaultTableModel) jtConsulta.getModel();
        modelo.setNumRows(0); // Utilizado para nao repetir as linhas ao clicarmos duas vezes em pesquisar.

        for (Pessoas_Model l : dados) {

            // inserindo valores nas linhas
            modelo.addRow(new Object[]{
                l.getId(),
                l.getNome(),
                l.getDocIdentidade(),
                l.getCpf(),
                l.getFicha()
            });
        }
    }

