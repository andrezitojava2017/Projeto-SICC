    private void jtConsultaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtConsultaMouseClicked

        // COMANDO QUE CAPTURA A LINHA QUE FOI SELECIONADA ATRAVES DO METODO GETSELECTROW()
        //PASSANDO PARA O ATRIBUTO LINHA. LOGO RECUPERAMOS O MODEL DA TABELA E CAPTURA
        //A INFORMAÇÃO QUE ESTA NA LINHA E COLUNA INDICADA NO METODO GETVALUEAT(LINHA,COLUNA)
        //PASSANDO PARA A CLASSE CADPESSOA;
        int linha = jtConsulta.getSelectedRow(); // capiturando a linha selecionada

        id_referencia = Integer.parseInt(jtConsulta.getModel().getValueAt(linha, 0).toString());

    }//GEN-LAST:event_jtConsultaMouseClicked

