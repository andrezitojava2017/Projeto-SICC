    private void btnLocallizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLocallizarActionPerformed
        JFileChooser janela = new JFileChooser();
        janela.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        
        // abre a janela e captura o retorno da opção escolhida
        int opt = janela.showOpenDialog(null);
        
        // verifica a opção escolhida
        if(opt == JFileChooser.APPROVE_OPTION){
            
            File caminho_escolhido = janela.getCurrentDirectory();
            
            destino.setText("caminho escolhido: " + caminho_escolhido.toString());
            
        }
    }//GEN-LAST:event_btnLocallizarActionPerformed

